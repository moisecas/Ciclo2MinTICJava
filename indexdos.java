public class indexdos {
    public static void main (String args []){
        var nombre = "moiso";
        System.out.println("nueva linea: \n" + nombre); //imprimir nueva linea
        System.out.println("tabulador: \t" + nombre); //imprimir tabulador
        System.out.println("retorno de carro: \b" + nombre); //retroceso un caracter hacia atrás en la impresión de la salida
        System.out.println("retorno de carro: \r" + nombre); //retroceso al inicio de la linea
        System.out.println("comilla simple: \'" + nombre + "\'"); //imprimir comilla simple
        System.out.println("comilla doble: \"" + nombre + "\""); //imprimir comilla doble
        


    } 
}
